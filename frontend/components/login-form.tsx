"use client"

import { useState } from "react"
import { useRouter } from "next/navigation"
import Link from "next/link"
import { Button } from "@/components/ui/button"
import { Input } from "@/components/ui/input"
import { api, UserRole } from "@/lib/api"
import { auth } from "@/lib/auth"

// Helper to get redirect path based on user role
function getRedirectPath(role: UserRole): string {
  switch (role) {
    case "superadmin":
      return "/dashboard/superadmin"
    case "tenant_admin":
      return "/dashboard/admin"
    case "client":
      return "/portal"
    case "patient":
      return "/portal"
    case "manager":
    case "user":
    default:
      return "/dashboard"
  }
}

export function LoginForm() {
  const router = useRouter()
  const [email, setEmail] = useState("")
  const [password, setPassword] = useState("")
  const [error, setError] = useState("")
  const [isLoading, setIsLoading] = useState(false)

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault()
    setError("")
    setIsLoading(true)

    try {
      // Basic validation
      if (!email || !password) {
        setError("Por favor ingrese email y contraseña")
        return
      }

      const response = await api.login({
        username: email,
        password: password,
      })

      auth.setToken(response.access_token)

      // Get user data to determine redirect
      const user = await api.getCurrentUser(response.access_token)
      const redirectPath = getRedirectPath(user.role)

      router.push(redirectPath)
    } catch (err: any) {
      console.error("Login error:", err)
      setError(err.message || "Credenciales inválidas. Verifique su email y contraseña.")
    } finally {
      setIsLoading(false)
    }
  }

  return (
    <div className="w-full">
      {/* Header */}
      <div className="text-center mb-8">
        <h1 className="text-2xl font-bold text-gray-900 mb-2">Clinic.online</h1>
        <h2 className="text-3xl font-bold text-gray-900 mb-4">¡Bienvenido de Nuevo!</h2>
        <p className="text-gray-600">
          ¿No tienes una cuenta?{" "}
          <Link href="/register" className="text-blue-600 hover:underline font-medium">
            Solicita acceso ahora.
          </Link>
        </p>
        <p className="text-sm text-gray-500 mt-1">
          Te contactaremos en menos de 24 horas.
        </p>
      </div>

      {/* Login Form */}
      <form onSubmit={handleSubmit} className="space-y-6">
        <div className="space-y-4">
          <Input
            id="email"
            type="email"
            placeholder="usuario@gmail.com"
            value={email}
            onChange={(e) => setEmail(e.target.value)}
            required
            disabled={isLoading}
            autoComplete="email"
            className="h-12 text-base"
          />
          
          <Input
            id="password"
            type="password"
            placeholder="Contraseña"
            value={password}
            onChange={(e) => setPassword(e.target.value)}
            required
            disabled={isLoading}
            autoComplete="current-password"
            className="h-12 text-base"
          />
        </div>

        {error && (
          <div className="text-sm text-red-600 bg-red-50 p-3 rounded-md">
            {error}
          </div>
        )}

        <Button 
          type="submit" 
          className="w-full h-12 bg-gray-900 hover:bg-gray-800 text-white font-medium text-base"
          disabled={isLoading}
        >
          {isLoading ? (
            <div className="flex items-center justify-center">
              <div className="animate-spin rounded-full h-5 w-5 border-b-2 border-white mr-2"></div>
              Ingresando...
            </div>
          ) : (
            "Ingresar Ahora"
          )}
        </Button>

        {/* Google Login Button */}
        <Button
          type="button"
          variant="outline"
          className="w-full h-12 border-gray-300 text-gray-700 font-medium text-base"
          disabled={isLoading}
        >
          <svg className="w-5 h-5 mr-3" viewBox="0 0 24 24">
            <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
            <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
            <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
            <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
          </svg>
          Ingresar con Google
        </Button>

        {/* Forgot Password Link */}
        <div className="text-center">
          <Link
            href="/forgot-password"
            className="text-gray-600 hover:text-gray-900 text-sm"
          >
            ¿Olvidaste tu contraseña? <span className="font-medium text-blue-600">Clic aquí</span>
          </Link>
        </div>
      </form>
    </div>
  )
}
